// Using escape sequences in strings
const singleQuote = 'It\'s a sunny day!';
const doubleQuote = "He said, \"Hello, World!\"";
const backslash = "This is a backslash: \\";
const newLine = "Line 1\nLine 2";
const tabbed = "Column 1\tColumn 2";

console.log(singleQuote);  // Output: It's a sunny day!
console.log(doubleQuote);   // Output: He said, "Hello, World!"
console.log(backslash);     // Output: This is a backslash: \
console.log(newLine); // Output: Line 1
//         Line 2
console.log(tabbed);        // Output: Column 1    Column 2